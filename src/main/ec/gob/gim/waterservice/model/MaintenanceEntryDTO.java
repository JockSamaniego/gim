package ec.gob.gim.waterservice.model;

import java.math.BigDecimal;

public class MaintenanceEntryDTO {
	
	private Long id;
	private BigDecimal value;
	
	public MaintenanceEntryDTO(){	
	}
	
	public MaintenanceEntryDTO(Long id, BigDecimal value){
		this.id = id;
		this.value = value;
	}
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public BigDecimal getValue() {
		return value;
	}
	public void setValue(BigDecimal value) {
		this.value = value;
	}

}
