package ec.gob.gim.waterservice.model;

import java.math.BigDecimal;
import java.util.Date;

import ec.gob.gim.revenue.model.MunicipalBond;

public class WrongWaterEmission {
	
	private Long municipalBondId;
	private Long municipalBondNumber;
	private Date serviceDate;
	private String residentName;
	private String residentIdentificationNumber;
	private String municipalBondGroupingcode;
	private BigDecimal municipalBondPaidtotal;
	
	private Consumption consumption;
	
	private MunicipalBond okMunicipalBond;
	
	public WrongWaterEmission(){
		
	}

	
	public WrongWaterEmission(Long municipalBondId, Long municipalBondNumber, Date serviceDate, String resident, 
							  String identificationNumber, String groupingCode, BigDecimal paidTotal){
		this.municipalBondId = municipalBondId;
		this.municipalBondNumber = municipalBondNumber;
		this.serviceDate = serviceDate;
		this.residentName = resident;
		this.residentIdentificationNumber = identificationNumber;
		this.municipalBondGroupingcode = groupingCode;
		this.municipalBondPaidtotal = paidTotal;
	}
	
	public Long getMunicipalBondId() {
		return municipalBondId;
	}

	public void setMunicipalBondId(Long municipalBondId) {
		this.municipalBondId = municipalBondId;
	}

	public Date getServiceDate() {
		return serviceDate;
	}

	public void setServiceDate(Date serviceDate) {
		this.serviceDate = serviceDate;
	}

	public String getResidentName() {
		return residentName;
	}

	public void setResidentName(String residentName) {
		this.residentName = residentName;
	}

	public String getResidentIdentificationNumber() {
		return residentIdentificationNumber;
	}

	public void setResidentIdentificationNumber(String residentIdentificationNumber) {
		this.residentIdentificationNumber = residentIdentificationNumber;
	}

	public String getMunicipalBondGroupingcode() {
		return municipalBondGroupingcode;
	}

	public void setMunicipalBondGroupingcode(String municipalBondGroupingcode) {
		this.municipalBondGroupingcode = municipalBondGroupingcode;
	}

	public BigDecimal getMunicipalBondPaidtotal() {
		return municipalBondPaidtotal;
	}

	public void setMunicipalBondPaidtotal(BigDecimal municipalBondPaidtotal) {
		this.municipalBondPaidtotal = municipalBondPaidtotal;
	}

	public Consumption getConsumption() {
		return consumption;
	}

	public void setConsumption(Consumption consumption) {
		this.consumption = consumption;
	}

	public MunicipalBond getOkMunicipalBond() {
		return okMunicipalBond;
	}

	public void setOkMunicipalBond(MunicipalBond okMunicipalBond) {
		this.okMunicipalBond = okMunicipalBond;
	}


	public Long getMunicipalBondNumber() {
		return municipalBondNumber;
	}


	public void setMunicipalBondNumber(Long municipalBondNumber) {
		this.municipalBondNumber = municipalBondNumber;
	}
	
	

}
