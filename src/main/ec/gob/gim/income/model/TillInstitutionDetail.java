package ec.gob.gim.income.model;

import java.math.BigDecimal;
import java.util.Date;

public class TillInstitutionDetail {
	
	private String name;
	private Date date;		
	private TillPermission tillPermission;
	private Integer tillNumber;
	private BigDecimal totalAccounts;
	private BigDecimal totalInterests;
	private BigDecimal totalSurcharges;
	private BigDecimal totalTaxes;
	private BigDecimal totalDiscounts;
	private BigDecimal totalPaymentAgreements; // abonos
	private BigDecimal subTotal;
	private BigDecimal commission;
	private BigDecimal total;

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public TillPermission getTillPermission() {
		return tillPermission;
	}
	public void setTillPermission(TillPermission tillPermission) {
		this.tillPermission = tillPermission;
	}
	public Integer getTillNumber() {
		return tillNumber;
	}
	public void setTillNumber(Integer tillNumber) {
		this.tillNumber = tillNumber;
	}
	public BigDecimal getTotalAccounts() {
		return totalAccounts;
	}
	public void setTotalAccounts(BigDecimal totalAccounts) {
		this.totalAccounts = totalAccounts;
	}
	public BigDecimal getTotalInterests() {
		return totalInterests;
	}
	public void setTotalInterests(BigDecimal totalInterests) {
		this.totalInterests = totalInterests;
	}
	public BigDecimal getTotalSurcharges() {
		return totalSurcharges;
	}
	public void setTotalSurcharges(BigDecimal totalSurcharges) {
		this.totalSurcharges = totalSurcharges;
	}
	public BigDecimal getTotalTaxes() {
		return totalTaxes;
	}
	public void setTotalTaxes(BigDecimal totalTaxes) {
		this.totalTaxes = totalTaxes;
	}
	public BigDecimal getTotalDiscounts() {
		return totalDiscounts;
	}
	public void setTotalDiscounts(BigDecimal totalDiscounts) {
		this.totalDiscounts = totalDiscounts;
	}
	public BigDecimal getTotalPaymentAgreements() {
		return totalPaymentAgreements;
	}
	public void setTotalPaymentAgreements(BigDecimal totalPaymentAgreements) {
		this.totalPaymentAgreements = totalPaymentAgreements;
	}
	public BigDecimal getSubTotal() {
		return subTotal;
	}
	public void setSubTotal(BigDecimal subTotal) {
		this.subTotal = subTotal;
	}
	public BigDecimal getCommission() {
		return commission;
	}
	public void setCommission(BigDecimal commission) {
		this.commission = commission;
	}
	public BigDecimal getTotal() {
		return total;
	}
	public void setTotal(BigDecimal total) {
		this.total = total;
	}
	
}