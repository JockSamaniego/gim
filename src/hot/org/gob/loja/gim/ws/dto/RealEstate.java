package org.gob.loja.gim.ws.dto;

public class RealEstate {
	
	/**
	 * <b>id</b> id Property
	 */
	private Long id;
	
	/**
	 * <b>code</b> Cadastral Code
	 */
	private String code;
	
	/**
	 * <b>numberProperty</b> number of house or lot
	 */
	private String numberProperty;
	
	/**
	 * <b>mainStreet</b> main Street of the property
	 */
	private String mainStreet;
	
	/**
	 * <b>nameNeighborhood</b> the name Neighborhood(Barrio) of the property
	 */
	private String nameNeighborhood;
	
	/**
	 * <b>identificationNumber</b> identificationNumber of the owner
	 */
	private String identificationNumber;
	
	/**
	 * <b>idBlock</b> id of the Block (manzana)
	 */
	private Long idBlock;
	
	/**
	 * <b>codeBlock</b> code of the Block (manzana)
	 */
	private String codeBlock;
	
	
	/**
	 * <b>cadastralCodeBlock</b> cadastralCode of the Block (manzana)
	 */
	private String cadastralCodeBlock;
	
	/**
	 * <b>streetBlockLimits</b> streets of the Block (Manzana)
	 */
	private String streetBlockLimits;
	
	/**
	 * <b>nameParish</b> name of the Parish (parroquia)
	 */
	private String nameParish;	
			
	/**
	 * <b>owner</b> owner from property
	 */
	private Taxpayer owner;
	
	public RealEstate(){
		owner = new Taxpayer();
	}

	public RealEstate(Long id, String code, String identificationNumber, String numberProperty,
			String mainStreet, String nameNeighborhood, Long idBlock, String codeBlock, 
			String cadastralCodeBlock) {
		this.id = id;
		this.code = code;
		this.identificationNumber = identificationNumber;
		this.numberProperty = numberProperty;
		this.mainStreet = mainStreet;
		this.nameNeighborhood = nameNeighborhood;
		this.idBlock = idBlock;
		this.codeBlock = codeBlock;
		this.cadastralCodeBlock = cadastralCodeBlock;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getNumberProperty() {
		return numberProperty;
	}

	public void setNumberProperty(String numberProperty) {
		this.numberProperty = numberProperty;
	}

	public String getMainStreet() {
		return mainStreet;
	}

	public void setMainStreet(String mainStreet) {
		this.mainStreet = mainStreet;
	}

	public String getNameNeighborhood() {
		return nameNeighborhood;
	}

	public void setNameNeighborhood(String nameNeighborhood) {
		this.nameNeighborhood = nameNeighborhood;
	}

	public Taxpayer getOwner() {
		return owner;
	}

	public void setOwner(Taxpayer owner) {
		this.owner = owner;
	}

	public String getIdentificationNumber() {
		return identificationNumber;
	}

	public void setIdentificationNumber(String identificationNumber) {
		this.identificationNumber = identificationNumber;
	}

	public Long getIdBlock() {
		return idBlock;
	}

	public void setIdBlock(Long idBlock) {
		this.idBlock = idBlock;
	}

	public String getCodeBlock() {
		return codeBlock;
	}

	public void setCodeBlock(String codeBlock) {
		this.codeBlock = codeBlock;
	}

	public String getCadastralCodeBlock() {
		return cadastralCodeBlock;
	}

	public void setCadastralCodeBlock(String cadastralCodeBlock) {
		this.cadastralCodeBlock = cadastralCodeBlock;
	}
	
	public String getStreetBlockLimits() {
		return streetBlockLimits;
	}

	public void setStreetBlockLimits(String streetBlockLimits) {
		this.streetBlockLimits = streetBlockLimits;
	}

	public String getNameParish() {
		return nameParish;
	}

	public void setNameParish(String nameParish) {
		this.nameParish = nameParish;
	}
	
}
